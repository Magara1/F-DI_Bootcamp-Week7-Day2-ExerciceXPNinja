package exercice1;

public class Student {
	
	//class attributes
	
	private String stdName;
	
	private double stdRollNo;
	
	private int stdId;
	
	//getters and setters

	public String getStdName() {
		return stdName;
	}

	public void setStdName(String stdName) {
		this.stdName = stdName;
	}

	public double getStdRollNo() {
		return stdRollNo;
	}

	public void setStdRollNo(double stdRollNo) {
		this.stdRollNo = stdRollNo;
	}

	public int getStdId() {
		return stdId;
	}

	public void setStdId(int stdId) {
		this.stdId = stdId;
	}
	
	

	
	

}
